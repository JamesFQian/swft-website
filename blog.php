				<span class="bodyTitle">The Enunciation Challenge</span> <br />
				<span class="author">By Paxton Attridge  |  December 1st, 2016</span> <br /><br />
			</p>
			<p> 
			Nothing destroys communication quite like poor articulation - we can't be moved by what you've said if we can't understand it. That said, a few helpful tools for maintaining control over diction - and the key to annunciation is exactly that, control - that can help coax silver tongues out of cotton mouths.
			<br /><br />
			<span class="subTitle">1. Know how to pronounce words.</span>
			<br /><br /> 
			This should go without saying, but beginning speakers often attempt to spice up their language with words that belong more in Shakespeare and legal dictionaries than in casual conversation. Comfort is key to control, as is the confidence that comes from familiarity.
			<br /><br />
			<span class="subTitle">2. Find the emphasis.</span>
			<br /><br />
			Every single word has at least one syllable of emphasis, and, as a macrocosm of this idea, each phrase has at least one emphasized word. Giving these key sections proper time, exerted pronunciation, and importance demonstrates that you understand what you are saying, and ensures that you don't flub the most critical components of your message.
			<br /><br />
			<span class="subTitle">3. Relax your mouth, but stay energized.</span>
			<br /><br />
			Muddled or incorrect diction is almost always the result of oral tension, whether too much or too little. Tongue twisters primarily help address this concern, stretching out and loosening your mouth while at the same time warming up the muscles needed for dextrous word choice. Some examples include the following:<br /></p>
			<p class="indent">"Unique New York, New York's unique, you know you need unique New York."<br /></p>
			<p class="indent">"Rubber baby buggy bumpers."<br /></p>
			<p class="indent">"Red leather, yellow leather, good blood, bad blood."<br /></p>
			<p class="indent">"I slit a sheet, a sheet I slit, and on that slitted sheet I sit."<br /></p>
			<p>
			Repeat these several times before speaking - nobody's too cool for vocal warm-ups. 
			<br /><br />
			<span class="subTitle">4. Stay hydrated.</span>
			<br /><br /> 
			A dry mouth leads to glitches in pronunciation - when dehydrated, the saliva on your tongue and palate become sticky, which increases the oral tension needed to say the same words as somebody who's indulged in their daily intake of H20. Speaking is a dehydrating experience - the more speaking engagements you have throughout the day, the more bottles of water you're going to need.
			<br /><br />
			<span class="subTitle">5. Slow down.</span>
			<br /><br />
			There's a 90% guarantee that you are going to fast, and if you're experiencing difficulties with diction, they're going to be magnified accordingly. Slowing down relaxes the mouth, gives you more time to address words (rather than accidentally truncating syllables), and forces you to spend time hammering in emphasis.
			<br /><br />
			<span class="subTitle">6. Don't be afraid of overemphasis.</span>
			<br /><br />
			Yes, overly-punctuated articulation can be off-putting, but it's always preferable to the alternative of slurring your words. It's also always easier to address overemphasis than the alternative.
			<br /><br />
			<span class="subTitle">7. Listen to yourself speak.</span>
			<br /><br />
			If you've attempted several of these steps and are still experiencing annunciation difficulties, recording your speech, and listening to it play back will help you self-diagnose difficulties with diction. It's awkward to hear the sound of your own voice, but it's preferable to not knowing whether others can understand you.
			<br /><br />
			None of these tips are substitutes for legitimate speech impediments - as one who's experienced - and overcome - a stutter and vocal tic, persistent, phonetic pronunciation problems are best solved by a speech therapist. That said, for anyone who's flubbed, muttered, rushed over, or mispronounced a word, remembering to take time and prepare with the steps above separates good speakers from great ones.
