				<span class="bodyTitle">The Importance of Presence in Public Speaking</span> <br />
				<span class="author">By James Qian  |  December 2nd, 2016</span> <br /><br />
				When Martin Luther King Jr. walked up to the podium to deliver his famous "I Have a Dream" speech, he seemed modest and humble. He had no physical presence; the towering Washington Monument and surrounding buildings made him seem almost insignificant. But once he started speaking, all eyes were on him. MLK used biblical phrases and metaphors, emphasizing the fact that he wasn't just talking, but giving a sermon.
				<br /><br />
				One of the biggest issues facing public speakers and forensics competitors is lack of presence. Since much of the competition centers around high school or collegiate students, older judges inherently treat them as less impactful. That's why mastering the art of presence is so important. Here are some quick tips to get you started.
				<br /><br />
				<span class="subTitle">1. Speak from your diaphragm.</span>
				<br /><br />
				All conversation is dependent on pushing air through your vocal chords, and choosing the right place to push air from is vital. Try pushing out from the bottom of your lungs, or your belly. Doing so will enable you to speak with power, at a lower pitch. This is incredibly important, since sounds with a lower pitch carry around the room and scream legitimacy. Additionally, speaking using only your vocal chords to control pitch and volume is exhausting, and can contribute to temporarily losing your voice.
				<br /><br />
				<span class="subTitle">2. Start speaking at the right volume.</span>
				<br /><br />
				Public speakers often fail to start out at the right volume, speaking so quietly that the air conditioning overpowers their voice. While a low volume might be fine for a conversation face to face, a large room presents additional challenges. Worse, speakers often think they're speaking too loud, when in reality they're not speaking loud enough. Think about it this way. If you need to speak louder than you would in normal conversation, you should not feel like you are speaking at an acceptable pitch. You should feel like you're too loud. If you feel that you're at the right volume, chances are, you're too quiet. One good tactic is aiming for the farthest corner from the room in terms of volume.
				<br /><br />
				<span class="subTitle">3. Speed</span>
				<br /><br />
				Not all of presence is entirely due to volume and pitch, however. Another secret is slowing down. When you speak louder, your words tend to echo more than normally. That's why slowing down is so critical, because it means your words won't echo back and forth and lose clarity. Additionally, staying slow shows to your audience that you're mature enough to have control, which helps give the impression of presence.
				<br /><br />
				<span class="subTitle">4. Physicality</span>
				<br /><br />
				Another factor is physical presence. This one should be self explanatory. Keep your feet a shoulder's width apart, and keep the gestures at a healthy minimum. Your shoulders should be square and your back straight. Nothing shows lack of commitment like slouching. Eye contact, most importantly, should be kept steady. Constantly panning the room smoothly does not look natural. Instead, look at one person steadily, then shift to the next person.
				<br /><br />
				Martin Luther King Jr's words have gone down in history, not just because of their content, but the way he delivered them. If he did not have the presence he did, few would have remembered him, let alone made him famous. Make sure to keep practicing presence! At worst, you'll spend a couple of minutes. But at best, you might go down in history.
